import { CATEGORIES, type Category, type Entry } from './schema';

const CAP = 200;

const CATEGORY_TITLES: Record<Category, string> = {
  workflows: 'Workflows',
  prompting: 'Prompting',
  cli: 'CLI',
  'memory-management': 'Memory Management',
};

export class BudgetError extends Error {
  constructor(public actual: number) {
    super(
      `dist/CLAUDE.md is ${actual} lines; cap is ${CAP}. ` +
        `Shorten summaries, retire entries, or split overflow into path-scoped rules.`,
    );
  }
}

export function emitClaudeMd(entries: Entry[]): string {
  const lines: string[] = [];
  lines.push('# Claude Code — Best Practices');
  lines.push('');
  lines.push('<!-- Generated from hub/ by agents/compile. Do not edit this file. -->');
  lines.push('<!-- Run `bun run compile` to regenerate. -->');
  lines.push('');

  const byCategory = new Map<Category, Entry[]>();
  for (const e of entries) {
    if (e.status === 'superseded') continue;
    if (!byCategory.has(e.category)) byCategory.set(e.category, []);
    byCategory.get(e.category)!.push(e);
  }

  for (const category of CATEGORIES) {
    const group = byCategory.get(category);
    if (!group || group.length === 0) continue;

    lines.push(`## ${CATEGORY_TITLES[category]}`);
    lines.push('');

    for (const e of group) {
      if (e.status === 'deprecated') {
        const replacement = e.supersedes[0];
        const note = replacement
          ? ` Replaced by \`${replacement}\`.`
          : ' No replacement recorded.';
        lines.push(
          `- **Deprecated:** \`${e.id}\` — ${e.summary}${note} ([source](${e.source.url}))`,
        );
      } else {
        lines.push(`- **${e.title}** — ${e.summary} ([source](${e.source.url}))`);
      }
    }
    lines.push('');
  }

  const output = lines.join('\n').replace(/\n+$/, '\n');
  const lineCount = output.split('\n').length;
  if (lineCount > CAP) throw new BudgetError(lineCount);
  return output;
}
