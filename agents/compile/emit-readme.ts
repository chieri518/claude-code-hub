import { CATEGORIES, type Category, type Entry } from './schema';

const CATEGORY_TITLES: Record<Category, string> = {
  workflows: 'Workflows',
  prompting: 'Prompting',
  cli: 'CLI',
  'memory-management': 'Memory Management',
};

function escapePipes(s: string): string {
  return s.replace(/\|/g, '\\|');
}

function entryPath(e: Entry): string {
  return `./claude-code/${e.category}/${e.id}.md`;
}

export function emitReadme(entries: Entry[]): string {
  const lines: string[] = [];
  lines.push('# Hub Index');
  lines.push('');
  lines.push('<!-- Generated by agents/compile. Do not edit this file. -->');
  lines.push('<!-- Run `bun run compile` to regenerate. -->');
  lines.push('');
  lines.push(
    'This is a human-browsable index of the knowledge base. The entries themselves ' +
      'live under [`claude-code/`](./claude-code/). The compiled distributable artifact ' +
      'lives at [`../dist/CLAUDE.md`](../dist/CLAUDE.md).',
  );
  lines.push('');

  const byCategory = new Map<Category, Entry[]>();
  for (const e of entries) {
    if (!byCategory.has(e.category)) byCategory.set(e.category, []);
    byCategory.get(e.category)!.push(e);
  }

  for (const category of CATEGORIES) {
    const group = byCategory.get(category);
    if (!group || group.length === 0) continue;

    lines.push(`## ${CATEGORY_TITLES[category]}`);
    lines.push('');
    lines.push('| ID | Title | Summary |');
    lines.push('| --- | --- | --- |');
    for (const e of group) {
      const idCell = `[\`${e.id}\`](${entryPath(e)})`;
      const titleCell = escapePipes(e.title);
      const summaryCell = escapePipes(e.summary.replace(/\s+/g, ' ').trim());
      const suffix = e.status === 'current' ? '' : ` _(${e.status})_`;
      lines.push(`| ${idCell} | ${titleCell}${suffix} | ${summaryCell} |`);
    }
    lines.push('');
  }

  return lines.join('\n').replace(/\n+$/, '\n');
}
